namespace SgTuggen\ContactBundle\Model;

/**
 * This class represents a contact message
 */
class Message implements MessageInterface
{
    /**
     * @string
     */
    protected $name;

    /**
     * @string
     */
    protected $email;

    /**
     * @string
     */
    protected $subject;

    /**
     * @string
     */
    protected $body;

    /**
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param string $name
     *
     * @return Message
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * @return string
     */
    public function getEmail()
    {
        return $this->email;
    }

    /**
     * @param string $email
     *
     * @return Message
     */
    public function setEmail($email)
    {
        $this->email = $email;

        return $this;
    }

    /**
     * Set the sender information
     *
     * @param string $name
     * @param string $email
     *
     * @return Message
     */
    public function setSender($name, $email)
    {
        $this->name = $name;
        $this->email = $email;

        return $this;
    }

    /**
     * @return string
     */
    public function getSubject()
    {
        return $this->subject;
    }

    /**
     * Set the subject
     *
     * @param string $subject
     *
     * @return Message
     */
    public function setSubject($subject)
    {
        $this->subject = $subject;

        return $this;
    }

    /**
     * @return string
     */
    public function getBody()
    {
        return $this->body;
    }

    /**
     * Set the message body
     *
     * @param string $body
     *
     * @return Message
     */
    public function setBody($body)
    {
        $this->body = $body;

        return $this;
    }
}
